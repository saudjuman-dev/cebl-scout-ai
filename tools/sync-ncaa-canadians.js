#!/usr/bin/env node
/**
 * Sync data/ncaa-canadians.json → ncaa-canadians-runtime.js
 *
 * The runtime file is loaded by the app via a <script> tag and exposes:
 *   window.NCAA_CANADIANS_META — { lastScraped, source, count, season }
 *   window.NCAA_CANADIANS      — full array of { name, school, pos, year, hometown }
 *   window.NCAA_BY_PROVINCE    — { ON: [...], QC: [...], BC: [...], AB: [...] … }
 *   window.NCAA_BY_SCHOOL      — { 'Maine': [...], 'Pacific': [...] … }
 *
 * The hand-curated `ncaaCanadians` array in data.js becomes a 24-player
 * "marquee/scouting" subset; we union the fresh 142+ list at app boot so
 * every render uses the canonical full roster, with the curated entries'
 * extra fields (ppg/rpg/apg/fit/note) layered on top.
 */

const fs = require('fs');
const path = require('path');

const IN_FILE  = path.join(__dirname, '..', 'data', 'ncaa-canadians.json');
const OUT_FILE = path.join(__dirname, '..', 'ncaa-canadians-runtime.js');
const dryRun = process.argv.includes('--dry-run');

if (!fs.existsSync(IN_FILE)) {
  console.error(`❌ Missing ${IN_FILE} — run scrape-ncaa-canadians.js first.`);
  process.exit(1);
}

const data = JSON.parse(fs.readFileSync(IN_FILE, 'utf-8'));
const players = data.players || [];

console.log(`→ Loaded ${players.length} players from ${data._lastScraped}`);

const PROV_RE = /\b(ON|QC|BC|AB|MB|SK|NS|NB|NL|PE|YT|NT|NU)\b/;

// Group by province
const byProvince = {};
for (const p of players) {
  const m = (p.hometown || '').match(PROV_RE);
  const prov = m ? m[1] : 'Other';
  if (!byProvince[prov]) byProvince[prov] = [];
  byProvince[prov].push(p);
}

// Group by school
const bySchool = {};
for (const p of players) {
  if (!bySchool[p.school]) bySchool[p.school] = [];
  bySchool[p.school].push(p);
}

const meta = {
  lastScraped: data._lastScraped,
  source: data._source,
  count: players.length,
  season: data.season || '2025-26',
  schools: Object.keys(bySchool).length,
  topSchool: Object.entries(bySchool).sort((a, b) => b[1].length - a[1].length).slice(0, 1)[0]?.[0] || ''
};

const banner = `// AUTO-GENERATED by tools/sync-ncaa-canadians.js — DO NOT EDIT BY HAND.
// Source: data/ncaa-canadians.json (scraped from NorthPoleHoops)
// Last scraped: ${data._lastScraped}
// Generated:    ${new Date().toISOString()}
//
// Loaded by index.html via <script src="ncaa-canadians-runtime.js"></script>
// Exposes:
//   window.NCAA_CANADIANS_META  — meta { lastScraped, source, count, season, schools }
//   window.NCAA_CANADIANS       — full array
//   window.NCAA_BY_PROVINCE     — grouped by Canadian province
//   window.NCAA_BY_SCHOOL       — grouped by school`;

const out = `${banner}

window.NCAA_CANADIANS_META = ${JSON.stringify(meta, null, 2)};

window.NCAA_CANADIANS = ${JSON.stringify(players, null, 2)};

window.NCAA_BY_PROVINCE = ${JSON.stringify(byProvince, null, 2)};

window.NCAA_BY_SCHOOL = ${JSON.stringify(bySchool, null, 2)};

// Helper: returns the fresh NCAA player record for a given name (diacritic-insensitive).
window.ncaaPlayerByName = function(name) {
  if (!name) return null;
  const k = name.toLowerCase()
    .normalize('NFD').replace(/[\\u0300-\\u036f]/g, '')
    .replace(/[^a-z\\s]/g, ' ')
    .replace(/\\s+/g, ' ').trim();
  for (const p of window.NCAA_CANADIANS) {
    const pk = p.name.toLowerCase()
      .normalize('NFD').replace(/[\\u0300-\\u036f]/g, '')
      .replace(/[^a-z\\s]/g, ' ')
      .replace(/\\s+/g, ' ').trim();
    if (pk === k) return p;
  }
  return null;
};
`;

console.log(`\n📊 By province:`);
for (const [prov, list] of Object.entries(byProvince).sort((a, b) => b[1].length - a[1].length)) {
  console.log(`   ${prov.padEnd(8)} ${list.length}`);
}

console.log(`\n📊 Top 8 schools:`);
const top = Object.entries(bySchool).sort((a, b) => b[1].length - a[1].length).slice(0, 8);
for (const [school, list] of top) console.log(`   ${school.padEnd(28)} ${list.length}`);

if (dryRun) {
  console.log('\n💧 DRY RUN — not writing.');
  process.exit(0);
}

fs.writeFileSync(OUT_FILE, out);
console.log(`\n💾 Wrote ${OUT_FILE} (${(out.length / 1024).toFixed(1)} KB)`);
