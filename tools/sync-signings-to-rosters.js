#!/usr/bin/env node
/**
 * Sync data/cebl-signings.json → runtime app data
 *
 * Reads:  data/cebl-signings.json (produced by scrape-cebl-signings.js)
 * Writes: cebl-signings-runtime.js  — fresh runtime export the app loads
 *
 * The runtime file is loaded by the app via a <script> tag and exposes:
 *   - window.CEBL_SIGNINGS: the full transactions array
 *   - window.CEBL_ROSTERS_2026: { [teamName]: [{ name, type, date }, ...] }
 *   - window.CEBL_PLAYER_TEAM: { [normalizedPlayerName]: latest team }
 *
 * The app then uses CEBL_PLAYER_TEAM to override stale `team` fields in
 * canadiansPro / importTargets at render time. This avoids destructive
 * edits to data.js (which is human-curated for scouting notes).
 *
 * Usage:
 *   node tools/sync-signings-to-rosters.js
 *   node tools/sync-signings-to-rosters.js --dry-run
 */

const fs = require('fs');
const path = require('path');

const IN_FILE = path.join(__dirname, '..', 'data', 'cebl-signings.json');
const OUT_FILE = path.join(__dirname, '..', 'cebl-signings-runtime.js');
const dryRun = process.argv.includes('--dry-run');

if (!fs.existsSync(IN_FILE)) {
  console.error(`❌ Missing ${IN_FILE} — run scrape-cebl-signings.js first.`);
  process.exit(1);
}

const data = JSON.parse(fs.readFileSync(IN_FILE, 'utf-8'));
const transactions = data.transactions || [];

console.log(`→ Loaded ${transactions.length} transactions from ${data._lastScraped}`);

// Normalize a player name for matching (strip accents, punctuation, jr/iii)
function normName(n) {
  return (n || '')
    .toLowerCase()
    .normalize('NFD').replace(/[̀-ͯ]/g, '')   // strip diacritics
    .replace(/\b(jr|sr|ii|iii|iv)\.?\b/g, '')           // strip suffixes
    .replace(/[^a-z\s]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

// Player → most recent team they signed with this season
const playerTeam = {};
// Team → list of {name, type, date}
const rosters = {};

// Sort oldest first so latest signing wins on overwrite
const sorted = [...transactions].sort((a, b) => (a.date || '').localeCompare(b.date || ''));

for (const t of sorted) {
  const isPlayerContract = /Player Contract|Developmental/i.test(t.type) ||
                           t.type === 'Standard Player Contract';
  if (!isPlayerContract) continue;   // skip head-coach/GM rows for roster building

  const k = normName(t.player);
  if (!k) continue;
  playerTeam[k] = t.team;

  if (!rosters[t.team]) rosters[t.team] = [];
  // Replace any prior entry for this player on the same team
  const existingIdx = rosters[t.team].findIndex(r => normName(r.name) === k);
  const entry = { name: t.player, type: t.type, date: t.date };
  if (existingIdx >= 0) rosters[t.team][existingIdx] = entry;
  else rosters[t.team].push(entry);
}

// Sort each roster: standard contracts first, then dev, then alpha
for (const team of Object.keys(rosters)) {
  rosters[team].sort((a, b) => {
    const aDev = /Dev/i.test(a.type) ? 1 : 0;
    const bDev = /Dev/i.test(b.type) ? 1 : 0;
    if (aDev !== bDev) return aDev - bDev;
    return a.name.localeCompare(b.name);
  });
}

// Coaching/GM additions per team (separate slice)
const coaches = {};
for (const t of transactions) {
  if (/Player Contract|Developmental/i.test(t.type)) continue;
  if (!coaches[t.team]) coaches[t.team] = [];
  coaches[t.team].push({ name: t.player, role: t.type, date: t.date });
}

const banner = `// AUTO-GENERATED by tools/sync-signings-to-rosters.js — DO NOT EDIT BY HAND.
// Source: data/cebl-signings.json (scraped from cebl.ca/transactions)
// Last scraped: ${data._lastScraped}
// Generated:   ${new Date().toISOString()}
//
// Loaded by index.html via <script src="cebl-signings-runtime.js"></script>
// Exposes:
//   window.CEBL_SIGNINGS       — full transaction list (most recent first)
//   window.CEBL_ROSTERS_2026   — { [team]: [{name, type, date}, …] }
//   window.CEBL_COACHES_2026   — { [team]: [{name, role, date}, …] }
//   window.CEBL_PLAYER_TEAM    — { [normalizedName]: currentTeam }`;

const out = `${banner}

window.CEBL_SIGNINGS_META = ${JSON.stringify({ lastScraped: data._lastScraped, source: data._source, count: transactions.length, season: data.season || '2026' }, null, 2)};

window.CEBL_SIGNINGS = ${JSON.stringify(transactions, null, 2)};

window.CEBL_ROSTERS_2026 = ${JSON.stringify(rosters, null, 2)};

window.CEBL_COACHES_2026 = ${JSON.stringify(coaches, null, 2)};

window.CEBL_PLAYER_TEAM = ${JSON.stringify(playerTeam, null, 2)};

// Helper for app.js: returns canonical 2026 team for a player name, or null.
window.ceblCurrentTeam = function(name) {
  if (!name) return null;
  const k = name.toLowerCase()
    .normalize('NFD').replace(/[\\u0300-\\u036f]/g, '')
    .replace(/\\b(jr|sr|ii|iii|iv)\\.?\\b/g, '')
    .replace(/[^a-z\\s]/g, ' ')
    .replace(/\\s+/g, ' ')
    .trim();
  return window.CEBL_PLAYER_TEAM[k] || null;
};
`;

console.log(`\n📊 Roster summary:`);
const teamSummary = Object.keys(rosters).sort();
for (const team of teamSummary) {
  const std = rosters[team].filter(r => !/Dev/i.test(r.type)).length;
  const dev = rosters[team].filter(r =>  /Dev/i.test(r.type)).length;
  console.log(`   ${team.padEnd(36)} ${String(std).padStart(2)} std + ${dev} dev = ${std + dev}`);
}
console.log(`\n📊 Coaches/management: ${Object.values(coaches).flat().length} entries across ${Object.keys(coaches).length} teams`);

if (dryRun) {
  console.log('\n💧 DRY RUN — not writing.');
  process.exit(0);
}

fs.writeFileSync(OUT_FILE, out);
console.log(`\n💾 Wrote ${OUT_FILE} (${(out.length / 1024).toFixed(1)} KB)`);
